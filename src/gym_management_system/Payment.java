    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        MemberInfo mem = new MemberInfo();
        String[] s = new String(jDateChooser1.getDate().toString()).split(" ", -1);
        String month = s[1];
        String date = s[2];
        String year = s[5];
        String gmail = jTextField2.getText();
        String amount = jTextField3.getText();
        String all = date + "-" + month + "-" + year;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        try {
            mem.setPayment(gmail, amount, all);
            String[] in = new String[3];
            in[0] = mem.getName(gmail);
            in[1] = all;
            in[2] = amount;
            model.addRow(in);

        } catch (Exception E) {
            JOptionPane.showMessageDialog(null, "Something Went Wrong", "Error", JOptionPane.INFORMATION_MESSAGE);
            setVisible(false);
            new Payment().setVisible(true);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

