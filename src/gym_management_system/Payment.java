    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String gmail = jTextField2.getText();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        MemberInfo mem = new MemberInfo();
        try {
            if (mem.isExistMember(gmail)) {
                jButton2.setEnabled(true);
                jButton3.setEnabled(false);
                ArrayList<String> payment = mem.getPayment(gmail);
                for (int i = 0; i < payment.size(); i++) {
                    String[] in = payment.get(i).split(" ", -1);
                    String[] pay = new String[3];
                    pay[0] = mem.getName(gmail);
                    pay[1] = in[1];
                    pay[2] = in[0];
                    model.addRow(pay);

                }
                Color col = new Color(250, 250, 250);

                jTextField3.setEditable(true);
                jButton2.setEnabled(true);
                jTextField2.setEditable(false);
                jTextField2.setBackground(new Color(200, 200, 200));

                jTextField3.setBackground(col);
                jTextField1.setText(mem.getName(gmail));
            } else {
                JOptionPane.showMessageDialog(null, "Gmail not Found", "Error", JOptionPane.INFORMATION_MESSAGE);

            }
        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Something Went Wrong", "Error", JOptionPane.INFORMATION_MESSAGE);
            setVisible(false);
            new Payment().setVisible(true);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

